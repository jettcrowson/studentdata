class Student

    def initialize(name, eyeColor, age, height, bloodtype)
    @name = name
    @eyeColor = eyeColor
    @age = age
    @height = height
    @bloodtype = bloodtype
    end
    def name
        return @name
    end
    def eyeColor
        return @eyeColor
    end
    def age
        return @age
    end
    def height
        return @height
    end
    def bloodtype
        return @bloodtype
    end


end